#include "libft.h"

void	ft_puttab(char **tab)
{
	int		i;

	i = -1;
	while (tab[++i])
	{
		ft_putstr("tab[");
		ft_putnbr(i);
		ft_putstr("] = ");
		ft_putendl(tab[i]);
	}
	return ;
}
